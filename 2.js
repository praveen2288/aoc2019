const SIZE = 4;

function compute(arr) {
  let splitArrays = [];

  for(let i=0; i<arr.length; i+=SIZE) {
    splitArrays.push(arr.slice(i, i+SIZE));
  }

  for(let i=0; i<splitArrays.length; i++) {
    let workingArray = splitArrays[i];
    let [opcode, input1pos, input2pos, position] = workingArray;
    if( opcode === 1) {
      arr[position] = arr[input1pos] + arr[input2pos];
    } else if (opcode === 2) {
      arr[position] = arr[input1pos] * arr[input2pos];
    } else if (opcode === 99) {
      return arr;
    }
  }
}

